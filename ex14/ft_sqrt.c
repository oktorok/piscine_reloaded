int	ft_sqrt(int nb)
{
	int i;
	int nb2;

	i = 1;
	nb2 = nb;
	if (nb2 > 1000000)
		nb2 = nb2 / 100;
	if (nb2 == 1)
		return (1);
	while (i < nb2 / 2)
	{
		if (i * i == nb)
			return (i);
		else
			i++;
	}
	return (0);
}
